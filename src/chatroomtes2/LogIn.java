    private void logBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_logBtnActionPerformed
        if (checkCaptcha()) { //checks if the captcha is correct after clicking the log in button
            if (findUser()) { 
                JOptionPane.showMessageDialog(rootPane, "Welcome");
                setName();
                this.setVisible(false);
                Client c = new Client(getName()); //makes a new client after log in process is finished
                c.clientRun();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Log in incorrect");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Captcha code is incorrect");
        }
    }//GEN-LAST:event_logBtnActionPerformed

