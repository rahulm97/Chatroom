    //this runs when log in hit and checks to see if the users login exists and allows them to log in
    public boolean findUser() {
        String name1 = userTxf.getText();
        String password1 = passTxf.getText();
        if (name1.length() < 1 || password1.length() < 1) {
            empty = true;
            System.out.println(empty);
            JOptionPane.showMessageDialog(rootPane, "Username or Password fields blank");
            return false;
        }
        String nameF;
        String passwordF;
        boolean right = false;
        try {
            Scanner sc = new Scanner(filename);

            while (sc.hasNextLine()) {
                nameF = sc.nextLine();
                System.out.println(nameF);
                passwordF = sc.nextLine();
                System.out.println(passwordF);
                if (nameF.equals(name1)) {
                    if (passwordF.equals(password1)) {
                        right = true;
                        break;
                    } else {
                        right = false;
                        break;
                    }
                } else {
                    right = false;

                }
            }
            sc.close();

        } catch (Exception e) {
            System.out.println("e");
        }
        return right;
    }

    //this is to register the user it checks if the log in already exists and then write to textfile
    public void signUp() {
        try {

            PrintWriter pw = new PrintWriter(new FileWriter(filename, true)); 
            String user1 = userTxf.getText();
            String pass1 = passTxf.getText();
            if (user1.length() < 1 || pass1.length() < 1) { //checks if the fields are empty
                empty = true;
                System.out.println(empty);
                JOptionPane.showMessageDialog(rootPane, "Username or Password fields blank");
            } else if (findUser()) { //checks if username and password exist
                JOptionPane.showMessageDialog(rootPane, "Username already exists");

            } else { //writes
                pw.println(user1);
                pw.println(pass1);
                JOptionPane.showMessageDialog(rootPane, "Registered");
            }
            pw.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    public boolean checkCaptcha() { //checks if the user entered the right captcha
        String check = captchaTxa.getText();
        String userCheck = captchaTxf.getText();
        boolean right1 = true;
        if (check.equals(userCheck)) {
            right1 = true;
        } else {
            right1 = false;
        }
        return right1;
    }

